    /*! \fn  public static int getLevelValMap(int level, int x, int y)
        \brief returneaza ce element am la o anumita coordonata pentru un anumit nivel

        \param level : numarul nivelului
        \param x : coordonata x
        \param y : coordonata y
     */
    public  int getLevelValMap(int level, int x, int y) {
        x = (x) / 50;
        y = y / 50;
        if (x < 3 * 21 && y < 15 && x >= 0 && y >= 0) {
            if (level == 1) {
                if (map1[y][x] == 18) {
                    return map1layer1[y][x];
                } else {
                    return map1[y][x];
                }
            } else if (level == 2) {
                return map2[y][x];
            } else {
                return map3[y][x];
            }
        }
        return 0;
    }

